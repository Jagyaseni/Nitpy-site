                               <p align="justify">The SC/ST/OBC Cell, a statutory body has been constituted by the Ministry of Human Resource Development (MHRD), Government of India (GOI) in National Institute of Technology Puducherry to safeguard the interests of the SC/ST & OBC students and employees of the Institute as per the instructions of MHRD, GOI from time to time.</p>

<p><b>CELL FUNCTION:</b></p>
<p>*   It functions to promote higher education among the SC/ST, OBC or weaker communities that are suffering
from economic, social and educational deprivations.</p>
<p>*   As a Grievances Redressal Cell for the grievances of SC/ST & OBC students and renders them necessary help in
solving their academic as well as administrative problems.</p>
<p>*   It continuously monitors and evaluates the reservation policies and other programs intended for
SC/ST & OBC by the GOI for their effective implementation at NIT Puducherry.</p>
<p>*   It suggests the follow-up measures to the administration of the Institute for achieving the objectives
and targets laid down by MHRD, GOI for the empowerment of SC/ST/OBC.</p>

<p><b>COMPLAINTS OF CASTE BASED DISCRIMINATION:</b></p>
<P> In case of any caste based discrimination of SC/ST/OBC Students / Staff, you can lodge your complaints from your email ID, with communication details, to <b>liaisonoffr@nitpy.ac.in</b></p>

