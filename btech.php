<h3>3. Admissions Under DASA Scheme</h3>
<p>Admissions are open for Foreign Nationals / Persons of Indian Origin (PIOs) / Non-Resident Indians (NRIs) under Direct Admission of Students Abroad (DASA) Scheme to National Institutes of Technology (NITs), IIITs, and other premier Technical Institutions.</p>
<h3>4. Joint Seat Allocation Authority (JoSAA) </h3>
<p>The <strong> Joint Seat Allocation Authority (JoSAA) 2018 </strong>has been set up by the Ministry of Human Resources Development (MHRD) to manage
and regulate the joint seat allocation for admissions to 100 Institutes for the academic year 2018-19. This includes 23 IITs, 31 NITs, 23 IIITs, and 23 other Government Funded Technical Institutes (Other-GFTIs). Admission to all the academic programs offered by these Institutes will be made through a single platform.<br>
For further details, please visit: <a href="http://josaa.nic.in" style="color: navyblue;" target="_new">http://josaa.nic.in</a></p>

