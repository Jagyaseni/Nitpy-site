<h3>1. Joint Entrance Examination (Main)</h3>
<p>Admission to Undergraduate Engineering Program for the candidates from India at NITs, IIITs, and other Centrally Funded Technical Institutions (CFTI) shall include the performance in the class 12/other Qualifying Examination and in the Joint Entrance Examination, JEE (Main).<br />
For further details, please visit: <a href="http://jeemain.nic.in/" target="_new">http://jeemain.nic.in</a></p>
<h3>2. Central Seat Allocation Board (CSAB)</h3>
<p>A Central Seat Allocation Board (CSAB) constituted by the Govt. of India coordinates admissions for the candidates from India to UG degree programmes in engineering, technology and architecture based on the All India Rank obtained through class 12/other Qualifying Examination and Joint Entrance Examination, JEE (Main). <!--CSAB 2013 is constituted in order to facilitate the seat allocation to students. The merit list (All India Rank) for admission to NITs and other CFTIs will be prepared by CBSE by giving 40% weightage (suitably normalized) to class XII (or equivalent examination) and 60% to the performance in JEE (Main) Examination.<br />
