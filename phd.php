<p><b>2. ELIGIBILITY</b></p>
<p><b>&nbsp;&nbsp;&nbsp;&nbsp;2.1 Ph.D. in Engineering </b></p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Candidates with Master’s degree in Engineering/Technology or a Master’s degree by
Research (M.S. By Research) in Engineering/Technology disciplines with a minimum
of 60% aggregate marks (or) CGPA of 6.5/10 in UG and in PG for admission under
OC/OBC category and 55% aggregate marks or equivalent CGPA of 6.0 for SC/ST
candidates. Aggregate marks/CGPA, the standard in awarding the degree that the
university follows will be taken for consideration. No conversion certificate is
required.<br>
<p><b>2.2 Ph.D. degree in Science & Humanities</b></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Candidates with First class Master’s degree in Sciences & Humanities in the relevant
discipline with minimum 60% aggregate marks (CGPA 6.5/10) in UG and in PG for
OC/OBC category and 55% aggregate marks or equivalent CGPA of 6.0 for SC/ST
candidates.
A valid GATE/UGC/CSIR/NET score is mandatory for full-time
candidates. Aggregate marks/CGPA, the standard in awarding the degree that the
university follows will be taken for consideration. No conversion certificate is
required.</p>
<p><b>Note:</b><br>
i. The award of scholarship/fellowship/assistantship shall be governed by circular
notification issued by NITPY from time to time.<br>
ii. Candidates applying for part-time and external modes of registration do not
require GATE score or NET Score or UGC/CSIR fellowship.<br>
iii. Research areas of all the departments are shown in Table I.<br>
iv. Final year PG students are eligible to apply provided that they should be able to
submit the PG provisional degree certificate and consolidated statement of grades
at the time of admission.<br>

<p><b>3. SELECTION PROCEDURE</b></p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Candidates recommended by the Departmental Selection Committee from the received
applications will be screened by an entrance test and/or interview by the Departmental
selection Committee with Chairman, Senate (or) a Senate nominee as observer. Both
full time and part time candidates will have a common written test.<br>
</p>

<p><b>4. ADMISSION PROCEDURE</b></p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a) Candidates whose selection is approved by the Director will be admitted to the
Ph.D. programme by paying the prescribed fees. The candidate should also pay
fees every semester as prescribed by the institute failing which registration will be
cancelled.<br>
</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;b) Ordinarily, a candidate is not eligible for re-registration for Ph.D. after
cancellation of his/her earlier registration for any reason. However, depending on
the merits and considering any special circumstance of the individual case, a
candidate may be considered for re-registration after Director's approval.
