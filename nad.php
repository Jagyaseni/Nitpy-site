<p> &nbsp; <b><font size="4" face="Arial, Helvetica, sans-serif"><center>NATIONAL ACADEMIC DEPOSITORY&nbsp;&nbsp;</center></font></b></p>
The Ministry of Human Resources Development (MHRD), Government of India, has come up with an innovative initiative, National Academic Depository  (NAD) that will digitally issuance, storage, and access all academic certificates issued by academic institutions in the country.
<BR><br>
Under NAD, every student will have a digital account to which all certificates issued by various academic institutions will be credited in digital format. Students will be identified uniquely by their Aadhaar number. NAD will eliminate paper, mitigate the risk of fake and forged certificates and enable online, electronic authentication of certificates by employers and higher education institutions.
<br><br>

All awards of the student in a single online NAD account. Online verification of all the awards is done with the consent of the student. 
<br><br>

NIT Puducherry signed an agreement with National Securities Depository Limited (NSDL) on "September 2017".  NSDL Database Management Limited (NDML) has been appointed as Depository of NAD. NDML is a subsidiary of "National Securities Depository Limited (NSDL)".
<br><br>
For more details related to student registration, certificate verification etc., please log on to the following links:
<a href="http://nad.gov.in" target="_new">http://nad.gov.in/</a><br>
<a href="https://nad.ndml.in/" target="_new">https://nad.ndml.in/</a>
<br><br>
To digitally access the certificates, the following are the simple online registration process:
<br><br>
1. Click on https://nad.ndml.in, click on Register button
<br>
2. Enter Aadhaar Number, provide consent to fetch  details from UIDAI
<br>
3. Enter OTP sent by UIDAI on your registered mobile no. / email ID.  
<br>
4. Choose User ID. / Password and registration is complete. 

