<h2>Research and Consultancy Office</a></h2>


<p>National Institute of Technology Puducherry (NITPy) is energetically involved in different areas of research and offers consultancy services to industry to bridge the gap between industry and academia. The research and consultancy activities are actively taken up by the faculty members of the Insttute and have been gaining momentum in the recent past. These activities help the faculty members to apply the concepts to practical problem and thereby gain the practical knowledge and share their gained knowledge effectively with the students. The research projects investigated by the faculty and sponsored by different government funding agencies have helped the Institute in developing state of the art facilities.  The institute is looking forward to expand its research vision with the addition of new faculty members/researchers, the implementation of policies designed to improve the research infrastructure, new academic and industry collaborations.
The Office of Research and Consultancy facilitates and coordinates all research and development activities of the Institute. </p>


