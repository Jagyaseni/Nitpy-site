<h2>Innovation Club</a></h2>


<p>Innovation club is an interdisciplinary club which primarily focuses on collecting innovative and creative ideas from students and transforming them into worthwhile projects. The ideas hence gathered can be put to meaningful use with careful approach and meticulous efforts to develop industrial standard products and obtain commercial outcomes.</p>
<h3><b>VISION</b></h3>
<ul>
<li>
<p>To create a platform where optimum knowledge and experience can be shared through experimentation of new ideas.</p>
</li>
<li>
<p>To develop industrial standard projects with collaborative efforts from students and teachers alike.</p>
</li>
</ul>
<h3><b>MISSION</b></h3>
<ul>
<li>
<p>To encourage active participation from every interested student in implementing the selected best ideas.</p>
</li>
<li>
<p>To provide financial aid to students with best creative ideas so as to encourage their endeavours of developing realisable projects.</p>
</li>
<li>
<p>To inculcate among students the basic skills of leadership, project management, marketing, entrepreneurship etc.</p>
</li>
</ul>
